def multiplicar(numero1,numero2):
  return  numero1 * numero2

def somar(numero1,numero2):
  return  numero1 + numero2

def subtrair(numero1,numero2):
  return  numero1 - numero2

def dividir(numero1,numero2):
  return  numero1 / numero2  


print('Eu quero multiplicar 2 x 3')
print(multiplicar(numero1=2,numero2=3)) 
print('Fim do programa')



print('Eu quero subtrair 2 - 2')
print(subtrair(numero1=2,numero2=2)) 
print('Fim do programa')


print('Eu quero somar 2 + 2')
print(somar(numero1=2,numero2=2)) 
print('Fim do programa')


print('Eu quero dividir 2 / 2')
print(dividir(numero1=2,numero2=2)) 
print('Fim do programa')

